package oops.inheritance;

public interface MyInterface {

     // Abstract Method : A method without a method body
     // Syntax for Abstract Method: access_specifier abstract return_type  function or method_name();
     // We will take only the public access_specifier in abstract method due to which other class can access or use their methods
     // An interface in Java is a blueprint of a class. It has static constants and abstract methods. The interface in Java is a mechanism to achieve abstraction.
     // Interface provides 100 percent abstraction because it has all methods as an abstract method

     public abstract void myMethod1 ();

     public abstract void myMethod2 ();




}
