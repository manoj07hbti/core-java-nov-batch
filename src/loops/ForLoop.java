package loops;

public class ForLoop {

    public static void main(String[] args) {

        // INCREMENT LOOP
        int number = 0;
        for (int i = 0; i < 10; i++) {
            System.out.println("HELLO WORLD "+i);
        }

        // DECREMENT LOOP

        for (int i = 10; i > 0; i--) {
            System.out.println("DECREMENT "+i);
        }
    }
}
