package loops;

public class ForLoop {

    public static void main(String[] args) {

        // INCREMENT LOOP
        int number = 0;
        for (int i = 0; i < 10; i++) {
            System.out.println("HELLO WORLD "+i);
        }

        // DECREMENT LOOP

        for (int i = 10; i > 0; i--) {
            System.out.println("DECREMENT "+i);
        }

        // revision
        for (int i = 0; i <5; i++) {
            System.out.println("Revision "+i);
        }

        // revision 2
        for (int i = 5; i>0; i--)  {
            System.out.println("Revision 2"+i);
        }

        }
    }

