package loops;

public class Demo {

    public static void main(String[] args) {


        //increment for loop

        for(int i=0; i<5; i++ ){

            System.out.println("Hello World "+i);
        }

        //decrement for loop

        for (int i=10; i>0; i--){

            System.out.println("Decrement Loop : "+i);
        }

    }
}
