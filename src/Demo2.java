public class Demo2 {
    public static void main(String[] args) {
        // SYNTAX Datatype varName = value;
        String name = "Salman Khan";
        System.out.println("the bollywood best actor is " + name);

        // SYNTAX Datatype varName = value;
        int age = 57;
        System.out.println("The age is " + age + " years ");

        // SYNTAX Datatype value = value;
        double income = 1000.50;
        System.out.println("The income is " + income + " cr ");
    }


}
