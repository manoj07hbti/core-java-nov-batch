package conditional_statement;

public class Switch_Statements_Months {

    public static void main(String[] args) {

        int month = 5;

        switch (month) {

            case 1: System.out.println("The month of january is going on");
            break;
            case 2: System.out.println("The month of february is going on");
            break;
            case 3: System.out.println("The month of march is going on ");
            break;
            case 4: System.out.println("The month of april is going on");
            break;
            case 5: System.out.println("The month of may is going on");
            break;
            case 6: System.out.println("The month of june is going on ");
            break;
            case 7: System.out.println("The month of july is going on");
            break;
            case 8: System.out.println("The month of august is going on");
            break;
            case 9: System.out.println("The month of september is going on");
            break;
            case 10: System.out.println("The month of october is going on");
            break;
            case 11: System.out.println("The month of november is going on");
            break;
            case 12: System.out.println("The month of december is going on");
            break;

            default: System.out.println("Invalid enter month name");
        }

    }

}
