package conditionalStatement;

public class Ifstatement {

    public static void main(String[] args) {

        int mark = 39;
        if (mark >= 60) {
            System.out.println("the candidate pass the exam by 1st division");
        }
        if (mark >= 40) {
            System.out.println("the candidate pass the exam bu 2nd division");
        }
        if (mark <= 40) {
            System.out.println("the candidate pass the exam with 3rd division");

        }
    }


}