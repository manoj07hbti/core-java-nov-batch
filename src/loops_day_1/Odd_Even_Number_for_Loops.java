package loops_day_1;

public class Odd_Even_Number_for_Loops {

    public static void main(String[] args) {

        // Odd and Even number
        for (int i=1; i<=100; i++){

            if (i%2==0){
                System.out.println("This is even number " + i);
            }
            else {
                System.out.println("This is odd number " + i);
            }


        }

    }


}
