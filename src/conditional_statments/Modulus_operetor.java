package conditional_statments;

public class Modulus_operetor {
    public static void main(String[] args) {


        // even number
         int number = 32;
         int result = number % 2;
         System.out.println("Even number is  " + result);


         // odd number
        int num = 25;
        result = num % 4;
        System.out.println("Odd num is  "+ result);

    }
}











