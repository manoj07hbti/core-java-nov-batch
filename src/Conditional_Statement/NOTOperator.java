package Conditional_Statement;

public class NOTOperator {

    public static void main(String[] args) {

        // EXAMPLE - 1
        int a = 10 , b = 20;
        if(!(a>b)) {
            System.out.println("FALSE");
        }
        else{
            System.out.println("TRUE");
        }

        // EXAMPLE - 2
        int num1 = 24 , num2 = 15;
        if(!(num1>num2)) {
            System.out.println("TRUE");
        }
        else{
            System.out.println("FALSE");
        }

    }
}
